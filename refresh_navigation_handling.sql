-- Trigger to update conversation read state when messages are read
CREATE OR REPLACE FUNCTION update_conversation_read_state()
RETURNS TRIGGER AS $$
DECLARE
    v_user_id UUID;
    v_other_user_id UUID;
    v_unread_count INTEGER;
BEGIN
    -- Get the user who is reading (not the sender)
    IF NEW.is_read = true AND OLD.is_read = false THEN
        -- Find the other user in the conversation
        SELECT 
            CASE 
                WHEN m.user1_id = NEW.sender_id THEN m.user2_id
                ELSE m.user1_id
            END INTO v_other_user_id
        FROM public.matches m
        WHERE m.id = NEW.match_id;

        v_user_id := v_other_user_id;

        -- Count remaining unread messages
        SELECT COUNT(*) INTO v_unread_count
        FROM public.messages
        WHERE match_id = NEW.match_id
        AND sender_id != v_user_id
        AND is_read = false
        AND is_deleted = false;

        -- Update or insert read state
        INSERT INTO public.conversation_read_state 
            (user_id, match_id, last_read_message_id, last_read_time, unread_count)
        VALUES (v_user_id, NEW.match_id, NEW.id, NOW(), v_unread_count)
        ON CONFLICT (user_id, match_id) 
        DO UPDATE SET
            last_read_message_id = EXCLUDED.last_read_message_id,
            last_read_time = EXCLUDED.last_read_time,
            unread_count = EXCLUDED.unread_count,
            updated_at = NOW();
    END IF;

    RETURN NEW;
END;
$$ LANGUAGE plpgsql;

CREATE TRIGGER trigger_update_conversation_read_state
    AFTER UPDATE OF is_read ON public.messages
    FOR EACH ROW
    EXECUTE FUNCTION update_conversation_read_state();

-- Trigger to update user session state
CREATE OR REPLACE FUNCTION update_user_session_timestamp()
RETURNS TRIGGER AS $$
BEGIN
    INSERT INTO public.user_session_state (user_id, updated_at)
    VALUES (NEW.user_id, NOW())
    ON CONFLICT (user_id) 
    DO UPDATE SET
        updated_at = EXCLUDED.updated_at;

    RETURN NEW;
END;
$$ LANGUAGE plpgsql;

CREATE TRIGGER trigger_update_user_session
    AFTER INSERT OR UPDATE ON public.conversation_read_state
    FOR EACH ROW
    EXECUTE FUNCTION update_user_session_timestamp();

