-- Create table for tracking user session state
CREATE TABLE IF NOT EXISTS public.user_session_state (
    user_id UUID PRIMARY KEY REFERENCES public.profiles(id) ON DELETE CASCADE,
    last_conversation_id UUID REFERENCES public.matches(id) ON DELETE SET NULL,
    last_message_check_time TIMESTAMPTZ DEFAULT NOW(),
    conversations_viewed_at TIMESTAMPTZ DEFAULT NOW(),
    messages_viewed_at TIMESTAMPTZ DEFAULT NOW(),
    session_start_time TIMESTAMPTZ DEFAULT NOW(),
    session_active BOOLEAN DEFAULT true,
    created_at TIMESTAMPTZ DEFAULT NOW(),
    updated_at TIMESTAMPTZ DEFAULT NOW()
);

-- Create table for conversation read state
CREATE TABLE IF NOT EXISTS public.conversation_read_state (
    id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    user_id UUID NOT NULL REFERENCES public.profiles(id) ON DELETE CASCADE,
    match_id UUID NOT NULL REFERENCES public.matches(id) ON DELETE CASCADE,
    last_read_message_id BIGINT REFERENCES public.messages(id) ON DELETE SET NULL,
    last_read_time TIMESTAMPTZ DEFAULT NOW(),
    unread_count INTEGER DEFAULT 0,
    created_at TIMESTAMPTZ DEFAULT NOW(),
    updated_at TIMESTAMPTZ DEFAULT NOW(),
    UNIQUE(user_id, match_id)
);

-- Create table for message sync tracking
CREATE TABLE IF NOT EXISTS public.message_sync_log (
    id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    user_id UUID NOT NULL REFERENCES public.profiles(id) ON DELETE CASCADE,
    match_id UUID NOT NULL REFERENCES public.matches(id) ON DELETE CASCADE,
    sync_type TEXT NOT NULL, -- 'full', 'incremental', 'refresh'
    messages_synced INTEGER DEFAULT 0,
    sync_status TEXT DEFAULT 'pending', -- 'pending', 'completed', 'failed'
    sync_start_time TIMESTAMPTZ DEFAULT NOW(),
    sync_end_time TIMESTAMPTZ,
    error_message TEXT,
    created_at TIMESTAMPTZ DEFAULT NOW()
);

