-- Function to handle browser refresh - restore conversation state
CREATE OR REPLACE FUNCTION handle_browser_refresh(
    p_user_id UUID,
    p_current_url TEXT DEFAULT NULL
)
RETURNS TABLE (
    refresh_handled BOOLEAN,
    should_sync_conversations BOOLEAN,
    last_conversation_id UUID,
    conversations_outdated BOOLEAN,
    messages_outdated BOOLEAN
) AS $$
DECLARE
    v_last_conversation_id UUID;
    v_last_refresh_time TIMESTAMPTZ;
    v_conversations_outdated BOOLEAN := false;
    v_messages_outdated BOOLEAN := false;
    v_should_sync BOOLEAN := false;
BEGIN
    -- Get user's last session state
    SELECT last_conversation_id, conversations_viewed_at, messages_viewed_at
    INTO v_last_conversation_id, v_last_refresh_time, v_last_refresh_time
    FROM public.user_session_state
    WHERE user_id = p_user_id;

    IF v_last_conversation_id IS NULL THEN
        v_last_conversation_id := NULL;
    END IF;

    -- Check if conversations are outdated (older than 5 minutes)
    IF v_last_refresh_time IS NULL OR v_last_refresh_time < NOW() - INTERVAL '5 minutes' THEN
        v_conversations_outdated := true;
        v_should_sync := true;
    END IF;

    -- Check if messages are outdated (older than 1 minute)
    IF v_last_refresh_time IS NULL OR v_last_refresh_time < NOW() - INTERVAL '1 minute' THEN
        v_messages_outdated := true;
    END IF;

    -- Update refresh timestamp
    UPDATE public.user_session_state
    SET updated_at = NOW()
    WHERE user_id = p_user_id;

    RETURN QUERY SELECT true, v_should_sync, v_last_conversation_id, v_conversations_outdated, v_messages_outdated;
END;
$$ LANGUAGE plpgsql SECURITY DEFINER;

-- Function to restore conversation after page reload
CREATE OR REPLACE FUNCTION restore_conversation_after_reload(
    p_user_id UUID,
    p_match_id UUID
)
RETURNS TABLE (
    restore_success BOOLEAN,
    conversation_data JSONB,
    messages JSONB[],
    unread_count INTEGER,
    last_read_message_id BIGINT
) AS $$
DECLARE
    v_conversation_data JSONB;
    v_messages JSONB[];
    v_unread_count INTEGER;
    v_last_read_message_id BIGINT;
BEGIN
    -- Verify user has access to conversation
    IF NOT EXISTS(
        SELECT 1 FROM public.matches 
        WHERE id = p_match_id 
        AND (user1_id = p_user_id OR user2_id = p_user_id)
    ) THEN
        RETURN QUERY SELECT false, NULL::JSONB, NULL::JSONB[], 0, 0::BIGINT;
        RETURN;
    END IF;

    -- Get conversation data
    SELECT jsonb_build_object(
        'match_id', m.id,
        'other_user_id', CASE WHEN m.user1_id = p_user_id THEN m.user2_id ELSE m.user1_id END,
        'other_user_name', CASE WHEN m.user1_id = p_user_id THEN p2.name ELSE p1.name END,
        'other_user_photos', CASE WHEN m.user1_id = p_user_id THEN p2.photos ELSE p1.photos END,
        'last_message_at', m.last_message_at,
        'created_at', m.created_at
    ) INTO v_conversation_data
    FROM public.matches m
    LEFT JOIN public.profiles p1 ON m.user1_id = p1.id
    LEFT JOIN public.profiles p2 ON m.user2_id = p2.id
    WHERE m.id = p_match_id;

    -- Get recent messages (last 50)
    SELECT array_agg(to_jsonb(msg.*)) INTO v_messages
    FROM (
        SELECT 
            id, sender_id, content, type, is_read, is_edited, created_at
        FROM public.messages
        WHERE match_id = p_match_id
        AND is_deleted = false
        ORDER BY created_at DESC
        LIMIT 50
    ) msg;

    -- Get read state
    SELECT unread_count, last_read_message_id
    INTO v_unread_count, v_last_read_message_id
    FROM public.conversation_read_state
    WHERE user_id = p_user_id AND match_id = p_match_id;

    IF v_unread_count IS NULL THEN
        v_unread_count := 0;
    END IF;

    IF v_last_read_message_id IS NULL THEN
        v_last_read_message_id := 0;
    END IF;

    RETURN QUERY SELECT true, v_conversation_data, v_messages, v_unread_count, v_last_read_message_id;
END;
$$ LANGUAGE plpgsql SECURITY DEFINER;

