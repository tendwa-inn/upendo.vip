-- Function to get all conversations that need refresh
CREATE OR REPLACE FUNCTION get_conversations_needing_refresh(
    p_user_id UUID,
    p_last_refresh_time TIMESTAMPTZ DEFAULT NULL
)
RETURNS TABLE (
    match_id UUID,
    other_user_id UUID,
    other_user_name TEXT,
    last_message_time TIMESTAMPTZ,
    last_read_time TIMESTAMPTZ,
    unread_count INTEGER,
    needs_refresh BOOLEAN
) AS $$
BEGIN
    -- If no last refresh time, use the user's last check time
    IF p_last_refresh_time IS NULL THEN
        SELECT COALESCE(messages_viewed_at, NOW() - INTERVAL '1 day')
        INTO p_last_refresh_time
        FROM public.user_session_state
        WHERE user_id = p_user_id;

        IF p_last_refresh_time IS NULL THEN
            p_last_refresh_time := NOW() - INTERVAL '1 day';
        END IF;
    END IF;

    RETURN QUERY
    SELECT 
        m.id as match_id,
        CASE 
            WHEN m.user1_id = p_user_id THEN m.user2_id 
            ELSE m.user1_id 
        END as other_user_id,
        CASE 
            WHEN m.user1_id = p_user_id THEN p2.name 
            ELSE p1.name 
        END as other_user_name,
        COALESCE(m.last_message_at, m.created_at) as last_message_time,
        COALESCE(crs.last_read_time, m.created_at) as last_read_time,
        COALESCE(crs.unread_count, 0) as unread_count,
        CASE 
            WHEN m.last_message_at > p_last_refresh_time THEN true
            WHEN crs.updated_at > p_last_refresh_time THEN true
            ELSE false
        END as needs_refresh
    FROM public.matches m
    LEFT JOIN public.profiles p1 ON m.user1_id = p1.id
    LEFT JOIN public.profiles p2 ON m.user2_id = p2.id
    LEFT JOIN public.conversation_read_state crs ON 
        crs.user_id = p_user_id AND crs.match_id = m.id
    WHERE (m.user1_id = p_user_id OR m.user2_id = p_user_id)
    AND (
        m.last_message_at > p_last_refresh_time
        OR crs.updated_at > p_last_refresh_time
        OR crs.last_read_time IS NULL
    )
    ORDER BY COALESCE(m.last_message_at, m.created_at) DESC;
END;
$$ LANGUAGE plpgsql SECURITY DEFINER;

-- Function to sync messages for a conversation
CREATE OR REPLACE FUNCTION sync_conversation_messages(
    p_user_id UUID,
    p_match_id UUID,
    p_last_sync_time TIMESTAMPTZ DEFAULT NULL
)
RETURNS TABLE (
    sync_completed BOOLEAN,
    messages_found INTEGER,
    new_messages INTEGER,
    updated_messages INTEGER,
    sync_time TIMESTAMPTZ
) AS $$
DECLARE
    v_messages_found INTEGER;
    v_new_messages INTEGER;
    v_updated_messages INTEGER;
    v_sync_time TIMESTAMPTZ := NOW();
BEGIN
    -- Record sync start
    INSERT INTO public.message_sync_log 
        (user_id, match_id, sync_type, sync_status, sync_start_time)
    VALUES (p_user_id, p_match_id, 'incremental', 'pending', v_sync_time);

    -- If no last sync time, get from conversation read state
    IF p_last_sync_time IS NULL THEN
        SELECT COALESCE(last_read_time, NOW() - INTERVAL '30 days')
        INTO p_last_sync_time
        FROM public.conversation_read_state
        WHERE user_id = p_user_id AND match_id = p_match_id;

        IF p_last_sync_time IS NULL THEN
            p_last_sync_time := NOW() - INTERVAL '30 days';
        END IF;
    END IF;

    -- Count messages found
    SELECT COUNT(*) INTO v_messages_found
    FROM public.messages
    WHERE match_id = p_match_id
    AND created_at > p_last_sync_time;

    -- Count new messages (unread)
    SELECT COUNT(*) INTO v_new_messages
    FROM public.messages
    WHERE match_id = p_match_id
    AND sender_id != p_user_id
    AND created_at > p_last_sync_time
    AND is_read = false;

    -- Count updated messages (edited or read status changed)
    SELECT COUNT(*) INTO v_updated_messages
    FROM public.messages
    WHERE match_id = p_match_id
    AND updated_at > p_last_sync_time
    AND created_at <= p_last_sync_time;

    -- Update sync log
    UPDATE public.message_sync_log
    SET sync_status = 'completed',
        sync_end_time = v_sync_time,
        messages_synced = v_messages_found
    WHERE user_id = p_user_id 
    AND match_id = p_match_id 
    AND sync_start_time = v_sync_time;

    -- Update user's last check time
    UPDATE public.user_session_state
    SET messages_viewed_at = v_sync_time,
        last_conversation_id = p_match_id
    WHERE user_id = p_user_id;

    RETURN QUERY SELECT true, v_messages_found, v_new_messages, v_updated_messages, v_sync_time;
END;
$$ LANGUAGE plpgsql SECURITY DEFINER;

-- Function to perform full refresh of all user conversations
CREATE OR REPLACE FUNCTION refresh_all_conversations(
    p_user_id UUID
)
RETURNS TABLE (
    refresh_completed BOOLEAN,
    conversations_updated INTEGER,
    total_new_messages INTEGER,
    refresh_time TIMESTAMPTZ
) AS $$
DECLARE
    v_conversations_updated INTEGER := 0;
    v_total_new_messages INTEGER := 0;
    v_refresh_time TIMESTAMPTZ := NOW();
    v_conversation RECORD;
BEGIN
    -- Record sync start
    INSERT INTO public.message_sync_log 
        (user_id, match_id, sync_type, sync_status, sync_start_time)
    VALUES (p_user_id, NULL, 'full', 'pending', v_refresh_time);

    -- Process each conversation
    FOR v_conversation IN 
        SELECT match_id 
        FROM get_conversations_needing_refresh(p_user_id, NULL)
        WHERE needs_refresh = true
    LOOP
        -- Sync this conversation
        PERFORM sync_conversation_messages(p_user_id, v_conversation.match_id);
        v_conversations_updated := v_conversations_updated + 1;
        
        -- Count new messages in this conversation
        SELECT COUNT(*) INTO v_conversation.new_messages
        FROM public.messages
        WHERE match_id = v_conversation.match_id
        AND sender_id != p_user_id
        AND is_read = false;
        
        v_total_new_messages := v_total_new_messages + v_conversation.new_messages;
    END LOOP;

    -- Update sync log
    UPDATE public.message_sync_log
    SET sync_status = 'completed',
        sync_end_time = v_refresh_time,
        messages_synced = v_total_new_messages
    WHERE user_id = p_user_id 
    AND sync_type = 'full'
    AND sync_start_time = v_refresh_time;

    -- Update user's last refresh time
    UPDATE public.user_session_state
    SET conversations_viewed_at = v_refresh_time,
        updated_at = v_refresh_time
    WHERE user_id = p_user_id;

    RETURN QUERY SELECT true, v_conversations_updated, v_total_new_messages, v_refresh_time;
END;
$$ LANGUAGE plpgsql SECURITY DEFINER;

