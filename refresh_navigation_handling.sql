-- Check sync status for all users
SELECT 
    uss.user_id,
    p.name as user_name,
    uss.session_active,
    uss.conversations_viewed_at,
    uss.messages_viewed_at,
    uss.last_conversation_id,
    COUNT(crs.match_id) as tracked_conversations
FROM public.user_session_state uss
JOIN public.profiles p ON uss.user_id = p.id
LEFT JOIN public.conversation_read_state crs ON uss.user_id = crs.user_id
GROUP BY uss.user_id, p.name, uss.session_active, uss.conversations_viewed_at, uss.messages_viewed_at, uss.last_conversation_id
ORDER BY uss.updated_at DESC;

-- Check sync performance
SELECT 
    user_id,
    sync_type,
    COUNT(*) as sync_count,
    AVG(EXTRACT(EPOCH FROM (sync_end_time - sync_start_time))) as avg_sync_time_seconds,
    SUM(messages_synced) as total_messages_synced,
    COUNT(CASE WHEN sync_status = 'failed' THEN 1 END) as failed_syncs
FROM public.message_sync_log
WHERE sync_start_time > NOW() - INTERVAL '24 hours'
GROUP BY user_id, sync_type
ORDER BY sync_count DESC;

-- Check for stale sessions (inactive for more than 30 minutes)
SELECT 
    user_id,
    session_active,
    updated_at,
    NOW() - updated_at as time_since_last_activity
FROM public.user_session_state
WHERE updated_at < NOW() - INTERVAL '30 minutes'
AND session_active = true;

-- Check conversation read state coverage
SELECT 
    p.id as user_id,
    p.name as user_name,
    COUNT(m.id) as total_conversations,
    COUNT(crs.match_id) as conversations_with_read_state,
    COUNT(m.id) - COUNT(crs.match_id) as missing_read_state
FROM public.profiles p
JOIN public.matches m ON p.id = m.user1_id OR p.id = m.user2_id
LEFT JOIN public.conversation_read_state crs ON p.id = crs.user_id AND m.id = crs.match_id
GROUP BY p.id, p.name
HAVING COUNT(m.id) > COUNT(crs.match_id)
ORDER BY missing_read_state DESC;