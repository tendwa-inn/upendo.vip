-- Function to handle navigation back to conversations list
CREATE OR REPLACE FUNCTION handle_navigation_to_conversations(
    p_user_id UUID
)
RETURNS TABLE (
    success BOOLEAN,
    conversations_count INTEGER,
    unread_total INTEGER,
    last_refresh_time TIMESTAMPTZ
) AS $$
DECLARE
    v_conversations_count INTEGER;
    v_unread_total INTEGER;
    v_last_refresh_time TIMESTAMPTZ;
BEGIN
    -- Update navigation timestamp
    UPDATE public.user_session_state
    SET conversations_viewed_at = NOW(),
        last_conversation_id = NULL,
        updated_at = NOW()
    WHERE user_id = p_user_id;

    -- Get conversation stats
    SELECT COUNT(*) INTO v_conversations_count
    FROM public.matches
    WHERE user1_id = p_user_id OR user2_id = p_user_id;

    SELECT COALESCE(SUM(unread_count), 0) INTO v_unread_total
    FROM public.conversation_read_state
    WHERE user_id = p_user_id;

    SELECT conversations_viewed_at INTO v_last_refresh_time
    FROM public.user_session_state
    WHERE user_id = p_user_id;

    RETURN QUERY SELECT true, v_conversations_count, v_unread_total, v_last_refresh_time;
END;
$$ LANGUAGE plpgsql SECURITY DEFINER;

-- Function to handle navigation to specific conversation
CREATE OR REPLACE FUNCTION handle_navigation_to_conversation(
    p_user_id UUID,
    p_match_id UUID
)
RETURNS TABLE (
    success BOOLEAN,
    conversation_exists BOOLEAN,
    unread_count INTEGER,
    last_message_time TIMESTAMPTZ,
    sync_status TEXT
) AS $$
DECLARE
    v_conversation_exists BOOLEAN;
    v_unread_count INTEGER;
    v_last_message_time TIMESTAMPTZ;
    v_sync_status TEXT;
BEGIN
    -- Check if conversation exists and user has access
    SELECT EXISTS(
        SELECT 1 FROM public.matches 
        WHERE id = p_match_id 
        AND (user1_id = p_user_id OR user2_id = p_user_id)
    ) INTO v_conversation_exists;

    IF NOT v_conversation_exists THEN
        RETURN QUERY SELECT false, false, 0, NULL::TIMESTAMPTZ, 'not_found'::TEXT;
        RETURN;
    END IF;

    -- Update navigation state
    UPDATE public.user_session_state
    SET last_conversation_id = p_match_id,
        updated_at = NOW()
    WHERE user_id = p_user_id;

    -- Get conversation stats
    SELECT unread_count, last_read_time
    INTO v_unread_count, v_last_message_time
    FROM public.conversation_read_state
    WHERE user_id = p_user_id AND match_id = p_match_id;

    IF v_unread_count IS NULL THEN
        v_unread_count := 0;
    END IF;

    -- Get sync status
    SELECT sync_status INTO v_sync_status
    FROM public.message_sync_log
    WHERE user_id = p_user_id AND match_id = p_match_id
    ORDER BY sync_start_time DESC
    LIMIT 1;

    IF v_sync_status IS NULL THEN
        v_sync_status := 'never_synced';
    END IF;

    RETURN QUERY SELECT true, true, v_unread_count, v_last_message_time, v_sync_status;
END;
$$ LANGUAGE plpgsql SECURITY DEFINER;

