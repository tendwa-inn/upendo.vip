-- Get all conversations for a user with last message
CREATE OR REPLACE FUNCTION get_user_conversations(user_uuid UUID)
RETURNS TABLE (
    match_id UUID,
    other_user_id UUID,
    other_user_name TEXT,
    other_user_photos TEXT[],
    last_message_content TEXT,
    last_message_time TIMESTAMPTZ,
    last_message_sender_id UUID,
    unread_count BIGINT,
    created_at TIMESTAMPTZ
) AS $$
BEGIN
    RETURN QUERY
    SELECT 
        m.id as match_id,
        CASE 
            WHEN m.user1_id = user_uuid THEN m.user2_id 
            ELSE m.user1_id 
        END as other_user_id,
        CASE 
            WHEN m.user1_id = user_uuid THEN p2.name 
            ELSE p1.name 
        END as other_user_name,
        CASE 
            WHEN m.user1_id = user_uuid THEN p2.photos 
            ELSE p1.photos 
        END as other_user_photos,
        last_msg.content as last_message_content,
        last_msg.created_at as last_message_time,
        last_msg.sender_id as last_message_sender_id,
        (
            SELECT COUNT(*) 
            FROM public.messages msg 
            WHERE msg.match_id = m.id 
            AND msg.sender_id != user_uuid 
            AND msg.is_read = false
        ) as unread_count,
        m.created_at
    FROM public.matches m
    LEFT JOIN public.profiles p1 ON m.user1_id = p1.id
    LEFT JOIN public.profiles p2 ON m.user2_id = p2.id
    LEFT JOIN LATERAL (
        SELECT content, created_at, sender_id
        FROM public.messages 
        WHERE match_id = m.id 
        ORDER BY created_at DESC 
        LIMIT 1
    ) last_msg ON true
    WHERE (m.user1_id = user_uuid OR m.user2_id = user_uuid)
    AND m.last_message_at IS NOT NULL
    ORDER BY m.last_message_at DESC;
END;
$$ LANGUAGE plpgsql SECURITY DEFINER;

-- Get messages for a specific conversation
CREATE OR REPLACE FUNCTION get_conversation_messages(match_uuid UUID, user_uuid UUID)
RETURNS TABLE (
    message_id BIGINT,
    sender_id UUID,
    sender_name TEXT,
    content TEXT,
    type TEXT,
    is_read BOOLEAN,
    is_edited BOOLEAN,
    created_at TIMESTAMPTZ
) AS $$
BEGIN
    RETURN QUERY
    SELECT 
        msg.id as message_id,
        msg.sender_id,
        p.name as sender_name,
        msg.content,
        msg.type,
        msg.is_read,
        msg.is_edited,
        msg.created_at
    FROM public.messages msg
    JOIN public.profiles p ON msg.sender_id = p.id
    WHERE msg.match_id = match_uuid
    AND EXISTS (
        SELECT 1 FROM public.matches 
        WHERE id = match_uuid 
        AND (user1_id = user_uuid OR user2_id = user_uuid)
    )
    ORDER BY msg.created_at ASC;
END;
$$ LANGUAGE plpgsql SECURITY DEFINER;

-- Send a message and update conversation
CREATE OR REPLACE FUNCTION send_message(
    p_match_id UUID,
    p_sender_id UUID,
    p_content TEXT,
    p_type TEXT DEFAULT 'text'
)
RETURNS BIGINT AS $$
DECLARE
    new_message_id BIGINT;
BEGIN
    -- Verify the sender is part of the match
    IF NOT EXISTS (
        SELECT 1 FROM public.matches 
        WHERE id = p_match_id 
        AND (user1_id = p_sender_id OR user2_id = p_sender_id)
    ) THEN
        RAISE EXCEPTION 'User is not part of this conversation';
    END IF;

    -- Insert the message
    INSERT INTO public.messages (match_id, sender_id, content, type)
    VALUES (p_match_id, p_sender_id, p_content, p_type)
    RETURNING id INTO new_message_id;

    RETURN new_message_id;
END;
$$ LANGUAGE plpgsql SECURITY DEFINER;

-- Mark messages as read
CREATE OR REPLACE FUNCTION mark_messages_as_read(
    p_match_id UUID,
    p_user_id UUID
)
RETURNS VOID AS $$
BEGIN
    UPDATE public.messages 
    SET is_read = true
    WHERE match_id = p_match_id 
    AND sender_id != p_user_id 
    AND is_read = false;
END;
$$ LANGUAGE plpgsql SECURITY DEFINER;

-- Get conversation stats
CREATE OR REPLACE FUNCTION get_conversation_stats(
    p_match_id UUID,
    p_user_id UUID
)
RETURNS TABLE (
    total_messages BIGINT,
    unread_count BIGINT,
    last_message_time TIMESTAMPTZ,
    other_user_last_active TIMESTAMPTZ
) AS $$
BEGIN
    RETURN QUERY
    SELECT 
        (SELECT COUNT(*) FROM public.messages WHERE match_id = p_match_id) as total_messages,
        (SELECT COUNT(*) FROM public.messages WHERE match_id = p_match_id AND sender_id != p_user_id AND is_read = false) as unread_count,
        (SELECT MAX(created_at) FROM public.messages WHERE match_id = p_match_id) as last_message_time,
        CASE 
            WHEN m.user1_id = p_user_id THEN p2.last_active_at
            ELSE p1.last_active_at
        END as other_user_last_active
    FROM public.matches m
    LEFT JOIN public.profiles p1 ON m.user1_id = p1.id
    LEFT JOIN public.profiles p2 ON m.user2_id = p2.id
    WHERE m.id = p_match_id
    AND (m.user1_id = p_user_id OR m.user2_id = p_user_id);
END;
$$ LANGUAGE plpgsql SECURITY DEFINER;

